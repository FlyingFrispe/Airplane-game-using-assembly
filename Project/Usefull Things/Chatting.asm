.model small
.stack 64
.data
MESSAGE DB 'Chatting$'
MESSAGES DB 30,?,30 DUP('?')
VALUE DB ?
.code
main proc far

MOV AH,0AH
LEA DX,MESSAGES
INT 21H
CALL NEWLINE
LEA SI,MESSAGES
MOV CL,[SI]+1
SEND_ALL_MESSAGE:
MOV AL,[SI]+2
MOV VALUE,AL
INC SI
MOV AH,2
MOV DL,VALUE
INT 21H
DEC CL
CMP CL,0
JNZ SEND_ALL_MESSAGE
call NEWLINE
main endp
NEWLINE PROC
MOV AH,2
MOV DL,0AH
INT 21H
MOV DL,0DH
INT 21H
RET
NEWLINE   ENDP
end main